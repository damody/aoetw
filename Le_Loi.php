          <p>黎利（1385年－1433年），越南後黎朝開國君主，後人稱為黎太祖。他起兵驅逐了明朝的駐軍，使越南（當時「大越」國土相當於今越南中、北部）最終取得獨立地位。</p>
          <p>黎利生于越南清化地區，在他的青年時期，陳朝被權臣胡季犛所篡，中國明朝籍此於1407年出兵佔領越南（當時國土相當於現今中、北部），設立了交趾承宣布政使司，使之成為中國轄地。與此同時，越南群雄並起，黎利亦於1418年發動藍山起義，從而展開了抗明十年戰爭。結果，明宣宗決定罷兵，黎利稱帝，使越南重獲獨立，開創後黎朝（1428─1789年）。另外，黎利獲明朝承認其對越南的統治，以保持中越的邦交關係。</p>
