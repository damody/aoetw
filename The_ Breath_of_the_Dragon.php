           <h3>主線故事和策略</h3>
          <div class="hrn1"></div>
          <div class="n1">
            <ol>
              <li>弗拉德·德古拉率領他的軍隊在地圖西北部的道路上，討論他們打擊奧斯曼軍隊的計劃。玩家約有8個火槍兵、4個弩兵、12個匈牙利輕騎兵、6個重裝長槍兵、4個長劍兵、10個北歐盾劍兵和6個重裝馬騎弓兵。火槍兵建議讓他們在路徑東側的森林中埋伏，以便敵人不知道是什麼打擊了他們，而匈牙利輕騎兵則建議在路徑的西邊埋伏，並準備從背後進行攻擊。請注意，奧斯曼帝國軍隊將在120秒內到達，如果玩家有任何單位站在路徑的中間，伏擊將會失敗，傷亡將會更大。奧斯曼帝國的軍隊將會有兩架火炮在最後方，雖然容易被擊殺，但對站在一起的我方單位是巨大威脅。玩家將所有遠程單位放在東面，而所有近戰單位放在西面。。</li>
              <li>在經過與奧斯曼帝國軍隊的戰鬥之後，德古拉將要在地圖中間取下由奧斯曼控制的朱爾朱（Giurgiu）。玩家將沿著小路向東南方向行進，並看到一個貿易馬車，玩家抓住它後德古拉會把自己偽裝成一個貿易商（表面上是國王），而在貿易車裡將會有兩個重裝長槍兵和兩個火槍兵。玩家將德古拉和貿易馬車帶入朱爾朱城。德古拉剩下的部隊將被留下，等待德古拉的信號。</li>
              <li>進入朱爾朱後，交易車會變成藏在裡面的四名士兵。玩家的目的是要奪取城堡，玩家必須試著把四個單位中的任何一個放到每個箭塔中。然而，玩家必須盡量不要讓四名士兵聚在一起，在市中心右邊的角落那裡有一名土耳其士兵，他們會起疑。如果是德古拉本人靠近有奧斯曼帝國騎士的那座斷橋，騎士也會懷疑，說出這位“交易者”看起來很熟悉的語音。每當任何奧斯曼守衛提出對玩家的懷疑，該單位必須盡快離開視線，否則在15秒後奧斯曼人將會攻擊玩家。使用國王來奪取除了離城堡最近的塔和橋兩邊的塔之外的所有塔。讓一個長槍兵駐紮在城堡附近，把另外兩名部隊放在火炮塔和城堡附近的塔裡。確定奪取所有的塔之後用剩餘的一位奪取旗幟旁的塔。這樣就引出土耳其火槍兵，讓玩家有足夠的時間使用長槍兵(其實任何單位都可)來奪取城堡。</li>
              <li>當玩家奪取城堡時，信號將被發送到德古拉的軍隊，他們將沖向朱爾朱，玩家必須擊敗奧斯曼守衛才能奪取整個久爾久。玩家應該用騎兵和火炮塔首先摧毀兩台衝撞車，因為它們對城堡和箭塔是一個很大的威脅，玩家必須保持這座城堡屹立不搖直到比賽結束。用遠程單位和步兵對付其他敵人單位。</li>
              <li>一旦奪取朱爾朱，玩家將準備征服剩下的五個奧斯曼城鎮。 讓你的部隊駐紮在城堡回血，玩家應該征服四個小城鎮破壞敵人的物資。他們會向玩家求和，並給予資源。現在馬上在斷掉的橋和兩座建塔中間圍至少兩座牆，在碼頭訓練戰船幫忙守護，另外城堡西邊也需要圍牆，這樣可以減緩黃色進攻的節奏。現在開始壯大你的經濟體並晉升到城堡時代。先訓練矛兵、長槍兵和裝甲步兵，上城堡時代馬上將他們升級，之後馬上往綠色進攻，歐布魯西薩只有弓兵、長槍兵跟戰船，你應該可以輕鬆擊敗他，用衝撞車撞毀城堡後他就會投降並給你資源。</li>
              <li>黃色在你上城堡時代前會有一隊以騎士、馬騎弓兵和馬札爾鏢驥兵的軍隊來犯，用長槍兵矛兵步兵應對，記得在兵工廠升級他們。如果有衝撞車用德古拉或是剩下的輕騎兵處理。等你處理完綠色之後就換他們了。用僧侶治療完東征部隊後馬上讓他們攻擊黃色，破壞他們的城鎮中心跟城堡他們就會投降。你也可以先用輕騎兵和德古拉到西北方他們的伐木場偷襲村民削弱他們的力量。</li>
              <li>南邊三國會不停的騷擾你，達斯特(灰色)會訓練火炮、土耳其火槍兵跟匈牙利輕騎兵；拉霍瓦會訓練戟兵；諾沃賽洛則訓練輕騎兵和戰矛兵，這三國也都會訓練海軍，其實最簡單獲得勝利的方式就是建立強大的海軍，升級到帝王時代研發化學，在碼頭訓練火炮戰船然後用重型弩炮戰船護航，拉霍瓦跟諾沃賽洛的城鎮中心，達斯特的城堡和世界奇觀(之後會蓋)都在射程範圍內，轟炸完成後派遣你的軍隊掃掉達斯特的城鎮中心贏得勝利。</li>
