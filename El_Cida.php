              卡斯提爾王國得到喘息，再度南下，收復重鎮瓦倫西亞與穆斯林進入另一個相持階段。
              <br>出生與比瓦爾，是具有摩爾人與西哥德血統的卡斯提爾貴族。「席德(El Cid)」則是穆斯林對他的敬稱，這個敬稱在他生涯中幾乎取代了本名。 席德的青年歲月很早就因為擔任卡斯提爾桑喬國王的禁軍隊長而嶄露頭角。並在1067年奉主君之命征伐萊昂的阿方索國王。但是桑喬國王並沒能坐穩王位就被暗殺，反而是被兼併的阿方索國王以弟弟的身份繼承了整個王國。
              由於曾經征討過阿方索，加上阿方索的近臣與巴塞羅那的貝倫格爾伯爵的排擠，席德被放逐，不得不投奔半島上的穆斯林王公蒙達米特，並為其服役了十年。若非1086年的穆斯林大軍壓境，也許他永遠無法回歸故土。
