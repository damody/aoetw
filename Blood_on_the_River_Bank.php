<h2>攻略</h2>
<div class="hrn1"></div>
<div class="n1">
 <ul>
<li>1.  城堡依然重要，一開始輕騎兵探索周圍環境。把六隻村民拉到營地北方，找到石礦點蓋城鎮中心，
這樣可以在自己不小心玩輸的時候有路可退，也可以快速爆村民。</li>

<li>2.  建議放棄原本的營地，因為周圍都是淺灘，不好蓋，很容易被攻陷。
直接找到營地東方有一攤金礦、石礦、動物的地方，蓋一座城堡與城鎮中心，把那裏當作主營地。</li>

<li>3.  蓋好城堡之後蓋一堆功能性的建築物擋路，然後專心爆經濟、點科技。</li>

<li>4.  綠色會先提出結盟，然後黃色才會提出結盟。建議這兩個結盟任務都要做，
黃色甚至會在你沒注意到的時候幫你完成推掉五座箭塔的任務。如果都不結盟的話，他們兩國的衝車會亂到你受不了。</li>

<li>5.  注意黃色要你給他1000金之後會立刻要你在十分鐘內去他們城內蓋城堡，如果你的石礦還不夠，千萬不能先給錢。</li>

<li>6.  綠色要你去把他們西方一座紅色堡壘裡面關著的公主救出來，只要把那個堡壘裡面所有的軍事建築以及箭塔拆掉，
就會獲得公主的控制權，此時綠色會先暫停對你的攻擊，但是公主必須平安送回綠色的城堡，否則綠色會翻臉。</li>

<li>7.  錢跟石礦夠了就先跟黃色結盟，綠色則等帝王後有好用的火炮，再配合馬弓、女兵、僧侶去推。
救公主的時候要經過綠色城堡，以及一些軍事設施，不要懷疑，就是要先推掉。
城堡推掉之後記得快速通過，因為那個城堡是任務建築，無論你打掉幾次，綠色村民都會來補蓋。</li>

<li>8.  救了公主之後，公主為了感謝你，會無限提供你飛刀女兵。（應該是bug），
因此救了公主之後你就可以用免費的女兵海直接淹死紅色取得勝利。</li>

</ul>

