          <h3>趣事</h3>
          <div class="hrn1"></div>
          <div class="n1">
            <ul>
              <li>和矛兵一樣是最便宜的單位。</li>
              <li>唯一沒有任何裝甲防禦的步兵單位。</li>
              <li>上天是公平的，沒有戟兵的文明大多有剋騎馬單位的駱駝，而沒有駱駝的，則是有厲害的重裝長槍兵（如阿茲特克與維京）或是克制馬的特殊兵種（如義大利）。</li>
              <li>雖然是叫做戟兵，但實際上武器是斧槍(瑞士斧)，只是初始的中文翻譯錯誤而已。</li>
              <li>戟兵的英文為Hellebarde，這個字有可能是來自德文字Halm（棍）與Barte（斧），其實斧槍與中國的戟並不相同，斧槍是斧與槍的結合，而戟則是戈與矛/槍的結合，所以翻譯成戟兵並不恰當。</li>
              <li>中國歷史上的戰神呂布，如果以武器來看，放到世紀帝國他只是一名戟兵而已，而且是垃圾單位。</li>
              <li>外觀倒是和潛水員有幾分神似。</li>
              <li>在征服者入侵1.0版本的時代，戟兵對於帝王時代的房屋有額外的加成，當然這是一個bug。</li>
            </ul>
          </div>
          <h3>歷史</h3>
          <div class="hrn1"></div>
          <div class="n1">
            <p>斧槍是一種雙手持握的長柄武器，在十四及十五世紀時最為風行。這個字有可能是來自德文字Halm（棍）與Barte（斧），現在德語稱此為Hellebarde。斧槍以大釘將斧刃固定在柄頭，通常有鉤或刺在斧刃的背面，這可以用來鉤住騎兵，
              斧槍通常長1.5至1.8公尺。斧槍與中國的戟並不相同，斧槍是斧與槍的結合，而戟則是戈與矛/槍的結合。</p>
            <p>斧槍在製造便宜，在戰場上的功能也很多。比槍與長槍更好用（也能用來推回騎兵），斧背上的鉤可以把騎兵拉到地上。</p>
            <p>此外，斧槍以金屬強化柄前端，可以有效的抵擋如刀劍等武器。一名瑞士農夫使用這個武器一擊殺死了勃根地公爵大膽的查理。</p>
            <p>斧槍是瑞士陸軍在十四世紀到十五世紀早期時的主要武器。之後，瑞士人增加了長槍來擊退騎士的進攻並席捲敵兵的方陣，而使用斧槍、長劍、匕首來進行近身戰鬥。德國人的國土僕人模仿瑞士人的戰鬥方式使用來長槍輔以斧槍來戰鬥，但隨身武器是選擇短劍。</p>
            <p>只要長槍兵相互交鋒時，斧槍就會成為當陷入長槍混戰時的有用武器，但是當需要保護慢裝填的鉤銃兵或火繩鳥銃兵避免受騎兵突襲時，斧槍兵的作用就減低了，大約在十六世紀中期，斧槍已經不在一般士兵的武器之中。</p>
            <p>斧槍曾被做為庭院保鑣的武器長達數個世紀，現在也是梵諦岡的瑞士衛隊的儀式武器。</p>
          </div>
