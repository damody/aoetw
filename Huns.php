              <p>匈人是一個古代生活在歐亞大陸的遊牧民族。他們在4世紀西遷到了東歐，並入侵東、西羅馬帝國。歐洲的古文獻對此一民族及其在歐洲的事蹟有若干敘述。5世紀末時，匈人國家逐漸瓦解並從歷史消失。匈人自東向西的進攻引發了歐洲民族大遷徙，最終導致西羅馬帝國滅亡，對歐洲的歷史產生了深遠影響。</p>
