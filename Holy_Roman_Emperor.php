          <li>以下攻略以最簡單取得四個遺跡為考量:勃艮地、斯瓦比亞、波希米亞跟奧地利是主要目標，這些城鎮沒有圍牆比較容易進攻。最初的目標請放在勃艮地，這座城鎮只有弓兵、矛兵跟一名僧侶保護，還有幾座箭塔，在城堡時代建造兩座馬廄用騎士摧毀看到的所有建築，再派輕型衝撞車摧毀城鎮中心勃艮地就會投降，你城鎮內的黃金很快就會用完，開採他們的黃金並升級所有單位。第二目標是斯瓦比亞，這個城鎮完全沒有防禦工事只有許多長槍兵，如果用騎士的話你會經歷不小的損失，不過還是可以擊敗他們。如果這個時候你已經是帝王時代就用火槍兵對付他們。</li>
