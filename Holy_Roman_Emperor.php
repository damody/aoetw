      <div class="hrn1"></div>
      <div class="n1">
        <ul>
          <li>一開始盡快派村民到四個河流淺灘處圍牆，然後在西邊、北邊和東邊的牆後各蓋一座塔駐守各兩個弓兵，投石車放在北邊的牆後幫忙防守。之後開始打羊跟豬發展經濟，記得在東北邊的金礦石礦區再蓋一個城鎮中心，升級垛牆後就專注升級到帝王時代。敵人會不時派軍隊騷擾你，駐紮弓兵或是火槍兵的箭塔是對付他們的最好選擇。</li>
