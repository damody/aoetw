           <h3>戰術</h3>

          <div class="hrn1"></div>
          <div class="n1">
            <ul>
              <li>碼頭是經濟和海戰的發展重點，因為碼頭能生產各種船隻，同時也是<a href="Fire_Ship.php">漁船</a>、<a href="Trade_Cog.php">貿易商旅</a>的基地。依據地圖的種類來建築、發展。</li>
              <li>當像是在群島的地圖中，碼頭就是一定要的，因為大部分的交戰都是在海上發生。建議在黑暗時代至少建造一個（最好是兩個）碼頭，以便通過生產漁船來提高經濟效益。</li>
              <li>在<a href="Coasal.php">沿海</a>、<a href="Baltic.php">波羅的海</a>、<a href="Continental.php">大陸</a>那類型的地圖上，雖然有一部分的水體，但是文明之間並沒有被水體隔開，在許多的情況下碼頭的影響力有限，但還是對經濟有一定的幫助，但海軍就只能用來支援陸上部隊、清除敵方沿海建築物，對遊戲勝敗影響較小。</li>
              <li>在<a href="Rivers">河流</a>、<a href="Highland.php">高地</a>或其他以河流或湖泊為特徵的地圖，碼頭可以成為防禦性結構的一部分，因為河流和湖泊能提供地面部隊防護。在這種狀況下，使用海俊部隊能夠更有效的讓防禦性建築靠近水邊，在這種地圖上的淺灘是陸地部隊可以穿過河流的唯一窄點。</li>
              <li>通常在碼頭附近蓋暸望箭塔和城堡是很有幫助的，因為這些建築能有效的保護碼頭避免敵船的攻擊，還可以將碼頭放在海灣中，因為這會使敵方海軍難以攻擊碼頭。也可以為碼頭提供很好的保護。</li>
            </ul>
