          <p>李篆是清化人，藍山起義發生不久後就去投奔黎利。1419年，明軍盡出攻打乂安。黎利見東都（今河內市）空虛，派李篆、范文巧、鄭可、杜秘等人攻打國威、廣威、嘉興、歸化、沱江、三帶（白鶴）、宣光等地，隨後引兵向東關城（今河內市）進軍，支援丁禮、阮熾所率的部隊。明將陳智至寧橋與應天阻攔，為李篆擊退，逃往寧江西岸。時有沐晟的雲南兵一萬人來支援，李篆遣范文巧阻擊，自己與杜秘率兵追擊陳智，大破之；隨後率軍支援范文巧，將雲南兵擊敗，破其退守三江城。事聞於明廷，罷免陳智、方政官職，戴罪立功。</p>
