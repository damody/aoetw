<h2>攻略</h2>
<div class="hrn1"></div>
<div class="n1">
 <ul>
<li>1.  來到本章的最終決戰了，這場戰役敵人會蓋世界奇觀取勝，粗估遊戲時間15分開始蓋，遊戲時間45分會蓋好，獲勝時間200年。</li>

<li>2.  敵人的海軍很強，但是不到大後期無法威脅到你的內陸，所以不用管它。一開始全力生村民採石頭，
島上升村民採食物。派1村走過陸橋，去地圖12點鐘方向有紅色城牆的橋上蓋圍牆。（注意敵人那兩排城牆的外圍是可以通過的，一樣要蓋牆圍起來）。
2村在圍牆後面蓋城堡。這裡特別注意城堡不是用來防守的，而是用來就近生產飛刀女兵，所以要蓋離圍牆遠一點，如果蓋太近，敵人會出動巨投直接把城堡砸掉。</li>

<li>3.  敵人第一波陸軍會在10分鐘左右走到圍牆邊，因此城堡蓋好之後立刻生飛刀女兵來防守。由於飛刀女兵屬於近戰傷害，
所以可以輕易將敵人的劍兵跟戟兵打趴。由於敵人中期會開戰船來攻擊你的飛刀女兵，所以記得要升幾隻僧侶來幫忙補血。如果敵人戰船太多威脅過大，就用飛刀女兵把戰船引到城堡附近給城堡射。</li>

<li>4.  以上步驟做好之後敵人基本上進不來。我們要開始做最後進攻準備。由於資源跟時間都十分有限，
不能等到後帝才進攻。你至少要點的科技一共有：兵工廠的弓兵相關科技以及步兵相關科技、大學的化學與建築生命值相關科技、
修道院加僧侶生命值與跑速的科技、磨坊的農田科技、靶場升級重裝馬騎弓兵、城堡升級精銳飛刀女兵、徵兵制度。</li>

<li>5.  點科技的同時，生出約十五隻火炮、20隻馬弓、20隻飛刀女兵。之所以用馬弓不用強弩，是因為馬弓跑得跟飛刀女兵差不多快，
一起行動才不會浪費了女兵的機動性。準備好之後把敵人橋上的城牆打掉一格改建為自己的城門。
大軍開過去，馬弓跟女兵固守原地模式作為掩護，火炮先拆城牆防禦工事，拆完打奇觀再掃蕩。要特別小心操控火炮的位置，
因為敵人兩層城牆之間放著許多弓兵以及巨投跟重投。小心別讓火炮被砸到了。</li>

<li>6.  你的死對頭橘色，他的軍隊全部圍在奇觀旁邊，由於時間緊迫不能節外生枝，
在你用火炮打奇觀的時候小心不要打到他的軍隊，他就不會弄你（真是豬隊友，隊友奇觀被攻擊都不會守一下）</li>

<li>7.  總體而言，敵人唯一棘手的是頻繁而大量的步兵。
（提示說是「未嘗一敗的步兵團來著？）只要方法對了，這關其實沒有網路上說的那麼難。</li>

<li>8.  將城牆內的軍事建築、生產建築、村民與部隊摧毀之後，紅色就會投降，而我們將獲得最終的勝利！</li>

</ul>

