                <li>當玩家到達克雷莫納時，當地的城堡會歸到你的麾下，但這座城堡也同時會被卡馬尼奧拉的許多火砲戰船攻擊。在這裡就要看點運氣了，看你是否來得及在城堡被摧毀前研發完成徵兵制度這個科技，這樣以後生產軍事單位時會快一些）PS：如果玩家一定要研發這個科技，就會建議在前往克雷莫納之前先存檔</li>
                <li>斯福爾扎一定要見到克雷莫納的首長，才好取得當地的兵營訓練軍隊，協助抵抗卡馬尼奧拉的入侵。入侵的軍隊由火槍兵與火砲組成，所以建議玩家只要訓練傭兵即可，因為他們對火藥單位有攻擊加成。卡馬尼奧拉的軍隊會集中火力攻擊西面的城牆，試圖進入克雷莫納。玩家一定要阻止他們，否則遊戲就算輸了。</li>
                <li>擊敗卡馬尼奧拉的軍隊後，斯福爾扎就準備坐船前往米蘭。河道上有卡馬尼奧拉的火炮船，一旦發現玩家就會進行攻擊。如果玩家損失僅有的兩台運輸船，在西方，也就是玩家登陸皮亞琴察那邊，會一直出現新的船隻來替代。</li>
                <li>一旦威尼斯戰船被摧毀，玩家必須準備面對卡馬尼奧拉並擊敗他的軍隊。此時，玩家基地附近的米蘭碼頭將由“卡馬尼奧拉的衛軍”接管。</li>
                <li>當斯福爾扎回到米蘭、見過公爵之後，玩家會接手帕維亞城，並準備面對威尼斯人的入侵。威尼斯人會入侵米蘭或者帕維亞，在這過程中，米蘭公爵必須存活、米蘭的城堡也不可以倒下。在這個階段，玩家會有一些村民可以採集資源，不過用處不大，因為他們極有可能被威尼斯的軍隊攻擊。</li>
                <li>因此，玩家應該盡早開始訓練軍隊，玩家也可以建造一隻水軍去摧毀威尼斯人在帕維亞東方的運輸船，那些運輸船裡載著火炮，是玩家極大的威脅。玩家在面對威尼斯人從帕維亞西方、西北方進犯的威尼斯軍隊時會吃不少苦頭，還有一些侵略者會搭著運輸船過來。</li>
                <li>當玩家成功擊敗威尼斯人的軍隊後，會看到馬拉泰斯塔家族的軍隊攻擊皮齊蓋托內城，以對抗卡馬尼奧拉，而卡馬尼奧拉的守軍會逃走。（皮齊蓋托內是卡馬尼奧拉的城市、馬拉泰斯塔是義大利本地的一支貴族世家）</li>
                <li>當馬拉泰斯塔的軍隊追擊卡馬尼奧拉的敗軍時，則會遭到卡馬尼奧拉的伏軍襲殺。此後，玩家、米蘭公爵以及馬拉泰斯塔就會被宣告勝利。</li>
                <li>在這個劇情裡，當斯福爾扎率眾會見米蘭公爵時，會出現菁英傭兵。在會見馬拉泰斯塔之前，米蘭公爵會讓一些聖騎士加入你的麾下。</li>
                <li>如果你沒有登上兩艘運輸船，只登上其中一艘，火砲戰船就不會攻擊你。如果你用巨型投石機殺掉所有準備伏擊馬拉泰斯塔軍隊的弩兵，勝利條件就不會被觸發。</li>
