            <div class="hrn1"></div>
            <div class="n1">
              <strong>優勢</strong>
              <ul>
                <li>哥德的步兵是遊戲最便宜，且生產也最快。</li>
                <li>哥德步兵若算上費用減免與特殊單位，則為遊戲中最強。</li>
                <li>哥德有早期的獵人優勢，但實際上沒什麼用。</li>
                <li>封建的步兵很便宜，可以用大量步兵壓制對手。</li>
                <li>軍營可以生產哥德衛隊，所以相對其他文明不需要太多石頭。</li>
                <li>海軍其實在平均水平，若有需要可以使用。</li>
                <li>哥德衛隊害怕近戰步兵，但是哥德有火槍手可以搭配哥德衛隊使用。</li>
                <li>弩砲可以有效克制步兵，但是哥德有完美的輕騎兵系可以克制它們。</li>
                <li>哥德的攻城武器並不差，攻城塔常常被忽略，但其實非常適合哥德。</li>
                <li>哥德弩兵在城堡時代其實很好用，但是大多數玩家不會猜到哥德出弩兵，可以讓對手無法反應。</li></ul>
              <strong>劣勢</strong>
              <ul>
                <li>哥德黑暗時代跟所有文明相同，並無任何強力的加成。</li>
                <li>由於沒有石牆與任何箭塔升級，哥德必須大量進攻，所以經濟被打擊的哥德將毫無用處。</li>
                <li>攻城武器雖然不差，但是毫無特色，也就是對於韓國這樣擁有火炮塔的防禦性文明將很難造成傷害。</li>
                <li>哥德單位害怕強力的近戰步兵，因為它們缺少步兵板甲，所以碰到豹勇士或是條頓武士這樣的單位則會吃大虧。</li>
                <li>哥德封建快攻很強力，但是若遭到防禦，後果可能是無法有足夠的後期經濟來壓制對手。</li>
                <li>房子被拆毀的哥德很容易被消滅。</li></ul>
              <strong>主要策略</strong>
              <ul>
                <li>哥德的封建快攻是毀滅性的，因為他能比其他文明生產更多的步兵，且速度更快。</li>
                <li>若來到城堡時代，哥德衛隊與長劍兵是很強勢的組合，好的哥德玩家必須熟知兵種相剋，善用便宜且升級完全的重裝長槍兵克制對手的騎兵，並且善用長劍兵對付對手的步兵，以保護哥德衛隊。</li>
                <li>城堡時代只要20個左右的哥德衛隊就可以強攻城鎮中心。</li>
                <li>在水上地圖，哥德並不差，但是大多數情況下，哥德玩家最好保持"乾燥"，因為海軍單位沒有減免。</p>
                  <li>哥德善於打擊弓箭文明，譬如瑪雅或是不列顛，但是對於擁有強大近戰步兵或是強力攻城武器的文明則很吃力，比如賽爾特。</li>
                  <li>由於沒有石牆，所以善用建築圍城則變得至關重要。</li>
                  <li>若遊戲進行到大後期，且經濟完善，毫無策略的無腦進攻就是最好的策略。</li>
                  <li>以進攻為防守就是哥德玩家必須遵守的核心概念。</li></ul>
              <strong>盟友選擇</strong>
              <ul>
                <li>阿茲特克擁有的豹勇士非常適合與哥德衛隊聯合進攻，且哥德的團隊加成能夠讓阿茲特克的步兵生產更加快速。</li>
                <li>斯拉夫的團隊加成尤其適合哥德(哥德通常有很多軍營)，再加上斯拉夫本身就強悍的步兵，甚至搭配貴族鐵騎也是可行的。</li>
                <li>義大利的傭兵於帝王時代可以在同盟的軍營生產，這使哥德可以擁有更多選擇，讓原本害怕大量火槍兵的哥德步兵團隊更加無敵。</li>
                <li>馬來擁有不需要花費黃金的雙手劍兵，且受益於哥德的團隊加成，可以用來保護哥德衛隊。</li>
                <li>其實哥德本身很強大，但是由於前期擁有劣勢，所以任何能夠在前期保護哥德的文明都是好的盟友選擇，譬如馬札爾。</li></ul>
              <strong>對抗要點</strong>
              <ul>
                <li>對上哥德的策略有兩種:在城堡時代之前消滅他們，或是在後期用投石車與火炮塔打陣地戰。</li>
                <li>哥德從封建時代開始增強，若是能在黑暗時代進行騷擾，則能對他造成很大的打擊。</li>
                <li>若哥德玩家在封建時代開始攻擊你，建造石牆能夠有效防禦。</li>
                <li>若你發現封建時代的哥德玩家沒有來進攻，那就換你必須顯得激進，以防止他們建立經濟，否則到了後期你將束手無策。</li>
                <li>封建時代的哥德沒有哥德衛隊，善用這一點好用弓兵進攻。</li>
                <li>城堡時代的哥德非常強大，但是仍然對於石牆加上投石車的防禦策略毫無辦法。</li>
                <li>大多數哥德玩家使用步兵，這就讓投石車顯得有利。</li>
                <li>帝王時代的哥德雖然強勢，但仍然害怕大量的火槍兵與投石車。</li>
                <li>擁有火炮塔與良好投石車的文明能夠有效防禦哥德玩家的進攻。</li></ul>
