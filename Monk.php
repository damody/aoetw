              <p>一群僧侶招降方式有兩種:
                <ol>
                  <li>每隻編一隊</li>
                  <li>clone操作</li>
                </ol>圈5隻僧侶對某敵兵按右鍵，這時就是5僧侶同時招1敵兵，但這時，對某中一僧侶按下ctrl+左鍵，就會取消選擇他，這時用沒有取消選擇的僧侶對另一敵兵按右鍵，這時就是4僧侶招1敵兵，被取消選擇那個就繼續招原本那敵兵。再用相同方法，取消選擇其中一僧侶，用剩下3僧侶招另一隻敵兵，如此類推。就會變成5僧侶在差不多時間招5隻敵兵了。另外，這方法也可以用於其他兵種的，不限定是僧侶，如用5
