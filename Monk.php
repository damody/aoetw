              <h3><a href="Relic.php">遺跡</a> </h3>
              <p><em>請見主條目：<a href="Relic.php">遺跡</a></em>
              </p>
              <p>遺跡是一個特殊的物品，當遺跡進入修道院時，擁有它的玩家將會隨著時間的推移而收到一小筆連續不斷的黃金，大致相當於一個礦工。玩家收集的遺跡越多，生成的黃金就越多。與採礦不同，遺跡不會耗盡。最重要的是，在城堡時代以後，快速而有效地訓練僧侶在標準勝利的遊戲中變得至關重要，因為在短時間內收集並持有每個聖物是贏得遊戲的眾多方式之一。</p>
              <h3>招降</h3>
              <p><em>請見主條目：<a href="Conversion.php">招降</a></em>
              </p>
              <p>僧侶的第二個功能是招降敵方單位。意見可以在遊戲中廣泛使用不同的轉換。然而，招降一個單位會使敵人的力量變小，同時增強自己的軍隊。在城堡時代，僧侶可以有效地支援快攻，對手沒有能力和時間研發異教邪說或使用如不列顛長弓兵的遠距離單位作反制。</p>
              <p>優異的僧侶文明（如阿茲特克人，<a href="Burmese.php">緬甸</a>，<a href="Saracens.php">薩拉森</a>，
                <a
                href="Slavs.php">斯拉夫</a>，<a href="Spanish.php">西班牙</a>和<a href="Teutons.php">條頓</a>）甚至可以進行僧侶快攻。這極具侵略性，因為被快攻的玩家很可能沒有研發異教邪說，若用僧侶反制則需花費大量的黃金。
                  <a
                  href="Portuguese.php">葡萄牙</a>也可以僧侶快攻，因為他們的僧侶便宜了15％，只花費85黃金。<a href="Teutons.php">條頓</a>的僧侶可以輕易地對抗早期僧侶快攻，因為他們的文明利益可讓單位更能抵抗招降（可與帝王時代的忠誠信仰疊加）。</p>
              <p>在<a href="Imperial_Age.php">帝王時代</a>，研發印刷技術可增加僧侶的招降距離，但是在帝王時代，玩家的軍隊變得更加強大，這使得僧侶更顯脆弱。幾乎所有的步兵和騎兵部隊都能三下殺死僧侶，弓箭手通常只需要五箭。攻城部隊對僧侶來說也是致命的。對於依賴僧侶的玩家來說，這是一個巨大的問題。此外，隨著忠誠信仰的出現，招降的效益顯著減少。另一個問題是招降需要大量的微操作，因為僧侶不會自動招降（僧侶在系統設定為非激進立場）。這意味著玩家總得時時注意戰場並手動招降。在遊戲的後期階段，當戰鬥較大，發生在多個地點時，幾乎不可能有時間指揮僧侶。如果只有一個主戰場，那麼玩家仍然可以招降遊俠，戰象或貴族鐵騎等關鍵單位，並且僧侶遠離威脅。</p>
              <h3>治療</h3>
              <p>僧侶能夠治療友方單位（每0.4秒恢復1生命 ）。僧侶通常搭配弓箭手，因為他們很容易控制，並且容易防守。但攻城武器對這個組合而言是一個嚴重威脅。玩家應該根據自己的部隊大小，搭配2到10名僧侶，在戰鬥中治療受傷部隊。僧侶應該盡量遠離危險，讓受傷的單位應該來到僧侶身邊，而不是讓僧侶衝向戰場，否則他們很容易成為目標。
                <a
                href="Byzantines.php">拜占庭</a>和<a href="Teutons.php">條頓</a>在遊戲中分別有更快的治癒率和更長的治療範圍。西班牙傳教士則有較快的移動速度可以跟上軍隊。</p>
              <h3>防禦</h3>
              <p>修道院不是軍事建築，且進入<a href="Castle_Age.php">城堡時代</a>後就可直接建造。這對於蓋<a href="Blacksmith.php">兵工廠</a>和
                <a
                href="Market.php">市場</a>用來進入城堡時代的爆經濟戰略是重要的。在達到城堡時代之後，玩家將很難走出定點，因此他們需要建造<a href="Barracks.php">軍營</a>、有
                  <a
                  href="Archery_Range.php">射箭場</a>和<a href="Stable.php">馬廄</a>，然後開始生產尚未升級的單位做防禦。相較之下，修道院可以馬上建造，僧侶可以立即生產，他們的功能齊全，搭配
                    <a
                    href="Siege_Workshop.php">攻城器製造所</a>的<a href="Mangonel.php">投石車</a>，玩家將可以最小的投資對<a href="Archer_Units.php">弓箭手</a>，
                      <a
                      href="Cavalry_Units.php">騎士</a>和<a href="Infantry_Units.php">步兵</a>的攻擊進行反制。隨著<a href="Stone_Wall.php">城牆</a>蓋起，留給對手的侵略性選項會進一步受到限制，因為他們不能再讓
                        <a
                        href="Eagle_Warrior.php">鷹勇士</a>或<a href="Scout_Cavalry.php">斥侯</a>隨意進你的陣地。</p>
