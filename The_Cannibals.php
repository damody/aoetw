            <div class="hrn1"></div>
            <div class="n1">
              <ul>
                <li>在這個劇情裡，玩家沒有太多的金礦，進入城堡、帝王時代相對困難、也沒辦法讓戰船升級。不僅如此，玩家也無法建造市集、更沒辦法與其他勢力交易，或者買賣資源。要取得黃金的唯一方法，就只能達成在地部落的要求、或者征服洛斯內格羅斯人以取得金礦的控制權。如同以往的劇情一般，玩家擔任印加人。</li>
                <li>西班牙征服者們分乘兩艘運輸船到達亞馬遜河的出海口，並且得知此地居有食人部落「洛斯內格羅斯」西班牙人們登陸的地方，也就是玩家應該建造基地的地方。</li>
                <li>有三個內格羅鷹勇士占據這個島嶼，他們可以輕易被消滅。當玩家在這座島上建立城鎮中心時，就會得知有些西班牙同胞被住在島上東方的洛斯內格羅斯人所俘虜，而玩家應該去拯救他們。</li>
                <li>玩家需要至少殺掉幾個洛斯內格羅斯守衛才能解救西班牙探險隊的隊長: 胡安.科特霍玩家必須摧毀資源儲存處才能取回探險隊們被沒收的配備。此後，胡安.科特霍會成為騎兵英雄、其他士兵則成為火槍兵，皆歸於玩家麾下。</li>
                <li>接著玩家就應該開始著手建造十五艘戰艦，以備出發前往大西洋、贏得遊戲。然而，在這個關卡，玩家可以取得的黃金卻是少之又少，而玩家也無法透過貿易取得黃金。（市集與貿易單位都無法使用）因此玩家應該接觸在西邊的兩個友好的部落：哈努阿庫和瑪卡帕巴，完成他們的要求，以取得做為回報的黃金。</li>
                <li>然後，玩家也可以攻擊在東邊的食人部落洛斯內格羅斯人，以攫取他們的黃金。</li>
                <li>想要取得任務要求，只需點擊紫色的單位並跟隨指示。每完成一個要求，就能得到兩百黃金。任務失敗則會取得較少黃金、或甚至沒有黃金。</li>
                <li>在哈努阿庫村莊中（位在玩家所在島嶼的北方），有一件謀殺案，受害者叫作 "蘇帕伊"，玩家應該幫助調查這個案子，而這個案子有四個嫌疑人正在受審。</li>
                <li>玩家必須點選村莊裡的其餘居民以取得他們的證詞，一旦玩家的視野回到審判中、下達判決之前，所有嫌疑犯都會堅稱自己沒有殺害蘇帕伊。認出嫌疑犯後，就用滑鼠點選你相信是兇手的那位。</li>
                <li>而無論你指控的是誰，你都必須在其他嫌疑犯被釋放前將之殺死。錯誤的指控會導致真兇消遙法外，而玩家也只能得到五單位的黃金。這時，真兇會自己承認罪行。</li>
