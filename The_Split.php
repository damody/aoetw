            </ul>
            <h2>戰術</h2>
            <div class="hrn1"></div>
            <div class="n1">
              <ul>
                <li>玩家首先必須擊敗當地住民埃爾巴科的反抗，然後他們的大酋長會投降並且答應幫助玩家。玩家需要獲得材料來建造運輸船並一邊處理敵對土著人的抵抗。然後玩家必須帶奧雷亞納和四個征服者離開皮薩羅並穿越亞馬遜河，征服伊馬拉並完成三個友好土著的要求來獲取寶貴的地區資料。</li>
                <li>開始時，玩家在地圖北端擁有一隊征服者、法蘭切斯科-德-奧雷亞納、岡薩洛-皮薩羅和戴利考拉(羽毛箭射手)在亞馬遜雨林身處尋找黃金城，你必須依賴當地人。向南端的河岸移動時，會找到一些埃爾巴科的漁船，也就是說村子就在附近。皮薩羅、奧雷亞納與戴利考拉必需生還，而戴利考拉必須小心保護，他不算是英雄單位，所以不會回復生命值。</li>
                <li>當玩家到埃爾巴科時，只需要摧毀所有的塔樓(海塔)，就能迫使他們投降。玩家可以很輕易的殺死敵軍士兵，但是槍隊長對屬於騎乘單位的征服者來說是很大的威脅。一旦塔被摧毀，大酋長將投降並同意協助玩家進行探索。</li>
                <li>玩家準備搭建一艘穿越亞馬遜河的船隻，但需要收集必要的材料。埃爾巴科會為玩家提供一個村民，從埃爾巴科東北部附近的倉庫(一個貿易工廠)收集材料。
                  然而，玩家會遇到敵對的本地人，並且他們正在嚴密監視倉庫。</li>
                <li>最好不要立即前往被嚴密監視的倉庫，而是向東南方去，有一些更容易被殺死的敵軍，前進直到一個可以取得僧侶的地方。僧侶對治療玩家的單位非常有用，特別是當玩家要對付守衛倉庫的敵人時。</li>
                <li>當村民到達了倉庫，會轉變成載貨的貿易車隊，並且會告訴玩家船帆被偷去放在別的地方，就在東方的一個小村莊裡，帆被做成旗幟插在那裏。玩家清除那裏的敵軍之後，貿易車隊就可以把船帆帶走並返回埃爾巴科的工作坊。</li>
                <li>玩家將獲得只足夠建造碼頭的木頭，建好了之後會自動獲得一艘西班牙的運輸船。運輸船上有四個征服者，奧雷亞納登上它，剩下的單位都會留下來。玩家應當將船沿亞馬遜河南行。</li>
                <li>隨著玩家繼續向南航行，該船將抵達一定的地點，在那裡它將遭受暴風雨襲擊，並在伊馬拉附近的角落沖向河岸西岸。船上的所有單位將被卸載，並且船將變回“西班牙”所屬。此時，如果奧雷亞納落後於其他地方，玩家將會輸掉。</li>
                <li>隨著玩家向南移動，伊馬拉的獨木舟會抵抗玩家，但這些可以很容易地被征服者隊殺死。一旦玩家進入伊馬拉，玩家必須殺死所有的士兵。即使有瞭望箭塔守衛村莊，玩家也不需要摧毀任何建築物。一旦軍隊被擊潰，伊馬拉酋長就會投降，除了柵欄牆和蓋茨以及首領本人之外，玩家將接管整個村莊。</li>
                <li>玩家將開始執行更多的任務，以更多地了解地形，同時在伊馬拉收集資源並建立軍隊。奧雷亞納需要親自拜訪三個友好村莊。</li>
                <li>伊馬拉西北部，最近的友好鄉村會要求拉上一隻爪哇野豬，以便他們的村民可以殺死牠作為食物。玩家應該派出一個村民，以打帶跑引誘爪哇野豬進入這個村莊。但是，玩家必須小心不要殺死爪哇野豬，否則戰役將失敗。</li>
                <li>上述村莊北部的友好村莊要求摧毀他們北部的敵對村莊。</li>
                <li>最遠的西部村莊要求將他們失去的12隻大羊駝從荒野中帶入由柵欄牆組成的破碎羊駝圈。然後，玩家只需添加一塊木牆即可修復羊駝圈。因此，不管怎樣，玩家都不應該簡單地殺死大羊駝，如果有7隻大羊駝被殺，玩家將會輸掉戰役。</li>
                <li>然而，北部敵對村莊的圈中有更多的大羊駝，可以彌補曠野中任何遇難的大羊駝。玩家在解放這些大羊駝時應該小心，因為這個敵對的村莊可能會將他們引誘到他們的城鎮中心並將其用作食物。</li>
              </ul>
