             <h2>芝麻小事</h2>
            <div class="hrn1"></div>
            <div class="n1">
              <ul>
                <li>建議馬上要做的第一件事就是盡快建立一支軍隊。首先建造一個城鎮中心，生產更多的村民並建造很多軍事建築，以便玩家能夠快速生產軍隊。軍隊應該由一些輕騎兵和韃靼騎兵以及很多馬弓騎兵組成。</li>
                <li>與此同時，派斥侯去探索地圖並找到敵方基地。軍隊建成後，派村民去收集資源。並在奄蔡附近部署軍隊，要帶一些村民。然後在那裏建造城堡，奄蔡應該會立刻反擊並攻擊城堡。將軍隊的設置為防衛狀態，攻城武器一但出現就立刻拿下。</li>
                <li>記得隨時補充軍隊。也不要忘記升級到帝王時代，會讓事情變得更容易。</li>
                <li>不久之後，奄蔡將耗盡軍隊，只會派出越來越少的單位，如果他們開始停止派出軍隊，就迅速將他們擊敗。</li>接下來是西哥德人。同樣使用對付奄蔡的戰術，但是這一次在城堡後面多建造一些塔來攻擊敵人的哥德衛隊(或是多用點近戰單位)。在攻城武器對城堡造成重大傷害之前派騎兵消滅他們。</li>
                <li>最後是西羅馬帝國。如果玩家仍然有很多資源，生產一些騎士，因為他們比輕騎兵更耐用。雙線夾擊西羅馬帝國，他們的軍隊會更快被耗盡，而且在城市被毀時，他們就無法在別處重建(堵住他們)。在他們的軍隊被耗盡之前，不要去攻擊。然後，製造一些炸藥桶或攻城武器來摧毀他們的城堡。如果玩家選擇攻城武器，他們必須被保護免受敵方近戰單位的傷害。然後摧毀他們的市鎮中心(如果他們試圖重建，請確保它們石頭用完了，或者摧毀它們）然後玩家所要做的就是殺死剩下的敵方單位並摧毀剩餘的敵方建築物。</li>
                <li>玩家會贏得這場戰役，但史實上並不是完全勝利(勝利後的遊戲解說有提到)。而是羅馬人和西哥特人的戰略性勝利。因為這場戰役阻止了匈奴征服羅馬高盧，並且使匈奴撤退。阿蘭斯被描述成維京人，但它們其實不是。</li>
                <li>奄蔡（上古漢語擬音：[ʔromʔ s.r̥ˁats]；英語：Alans，或 Alani，也拼為 Alauni 或 Halani）為古代中亞印歐語系遊牧民族，又作闔蘇，1—3世紀中葉的
                  東漢三國時期也稱阿蘭聊（中古漢語擬音：[ʔɑ lɑn leu]）或阿蘭。他們族源是塞克人—薩爾馬特人。遊戲舊版初期翻譯為亞蘭人，現漢化翻譯為奄蔡</li>
